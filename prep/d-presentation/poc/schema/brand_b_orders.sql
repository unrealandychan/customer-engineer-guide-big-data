-- =============================================================
-- Key schema pain points (for presentation talking points):
--   1. customer_uuid is a UUID — incompatible with Brand A integer, Brand C loyalty string
--   2. Date-only granularity (no timestamp) — cannot correlate with Brand A/C event times
--   3. Gross vs. net distinction — must choose one canonical "amount" field
--   4. Currency code present (multi-currency) — Brand A assumes USD only
--   5. Source is on-prem Oracle → nightly batch → no real-time option without CDC (debezium)
--   6. SAP material numbers (sku) don't map to Brand A/C SKUs — separate product resolution needed
-- =============================================================
