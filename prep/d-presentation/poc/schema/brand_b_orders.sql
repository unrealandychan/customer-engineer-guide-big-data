-- =============================================================
-- Brand B: Raw Orders Schema
-- Source system: SAP ERP → Oracle DB (on-premises)
--               Nightly batch export → SFTP → GCS → BigQuery
-- Customer identity: customer_uuid (UUID STRING) — SAP-generated
-- Load pattern: Nightly batch at 02:00 local time (one day lag)
-- Notes: Stores gross + net separately (pre/post-tax amounts).
--        No channel split — all transactions assumed in-store
--        (Brand B is purely brick-and-mortar).
--        txn_date is a DATE (not TIMESTAMP) — time granularity lost.
-- =============================================================

-- BigQuery DDL (raw ingestion layer — append only)
CREATE TABLE IF NOT EXISTS `{PROJECT_ID}.raw_brand_b.orders` (
  ord_number       STRING    NOT NULL,  -- SAP sales order number (e.g. "0000183940")
  customer_uuid    STRING    NOT NULL,  -- SAP customer UUID — globally unique within Brand B
  sku              STRING    NOT NULL,  -- SAP material number (e.g. "MAT-B-00348")
  location_code    STRING    NOT NULL,  -- SAP plant/store code (e.g. "PLANT-07")
  txn_date         DATE      NOT NULL,  -- Transaction DATE only — no time component
  gross_amount     NUMERIC   NOT NULL,  -- Pre-tax order value (local currency)
  net_amount       NUMERIC   NOT NULL,  -- Post-tax order value (local currency)
  currency_code    STRING    NOT NULL,  -- ISO 4217 (e.g. 'USD', 'CAD')
  _batch_date      DATE,                -- Nightly batch run date (pipeline metadata)
  _source_file     STRING               -- GCS SFTP drop file path
)
PARTITION BY txn_date
CLUSTER BY location_code
OPTIONS (
  description = "Brand B raw orders — SAP nightly batch export. Partition by txn_date, cluster by location_code (store). No time component available — date only."
);

